import os


class Calculations:
    
    def __init__(self,a,b) :
        self.a=a
        self.b=b
        
    def print_string(input):
        print("String is :"+input)
        
    def get_sum(self):
        return self.a+self.b
    
    def get_diff(self):
        return self.a-self.b
    
    def get_pro(self):
        return self.a*self.b
    
    def get_quo(self):
        return self.a*self.b